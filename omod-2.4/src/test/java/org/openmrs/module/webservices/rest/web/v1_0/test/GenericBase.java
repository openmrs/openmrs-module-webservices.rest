import org.openmrs.BaseOpenmrsObject;

public class GenericBase<T> extends BaseOpenmrsObject {
	
	private Integer id;
	
	private T value;
	
	@Override
	public Integer getId() {
		return id;
	}
	
	@Override
	public void setId(Integer id) {
		this.id = id;
	}
	
	public T getValue() {
		return value;
	}
	
	public void setValue(T value) {
		this.value = value;
	}
}
